  <script>
    let currentStep = 1;
    let selectedTime = '';
    let selectedDate = '';
    const currentMonth = 4;
    const currentYear = 2025;

    function nextStep(step) {
      document.querySelector(`#step${currentStep}`).classList.remove('active');
      document.querySelector(`#step${step}`).classList.add('active');
      currentStep = step;
      if (step === 2) populateTimeSlots();
      if (step === 4) showSummary();
    }

    function populateTimeSlots() {
      const slots = ["10:00am", "10:30am", "11:00am", "11:30am", "12:00pm", "12:30pm"];
      const container = document.getElementById("timeSlots");
      container.innerHTML = '';
      slots.forEach(time => {
        const btn = document.createElement('button');
        btn.type = "button";
        btn.textContent = time;
        btn.onclick = () => {
          document.querySelectorAll('.time-slot button').forEach(b => b.classList.remove('selected'));
          btn.classList.add('selected');
          selectedTime = time;
        };
        container.appendChild(btn);
      });
    }

    function showSummary() {
      const name = document.getElementById("name").value;
      const email = document.getElementById("email").value;
      const mobile = document.getElementById("mobile").value;
      const condition = document.getElementById("condition").value;
      const address = document.getElementById("address").value;
      const gender = document.getElementById("gender").value;

      document.getElementById("summary").innerHTML = `
      <strong>Name:</strong> ${name}<br>
      <strong>Email:</strong> ${email}<br>
      <strong>Mobile:</strong> ${mobile}<br>
      <strong>Health Condition:</strong> ${condition}<br>
      <strong>Address:</strong> ${address}<br>
      <strong>Gender:</strong> ${gender}<br>
      <strong>Appointment:</strong> ${selectedDate} at ${selectedTime} (45 min)
    `;
    }

    function prepareForm() {
      document.getElementById("selected_date").value = selectedDate;
      document.getElementById("selected_time").value = selectedTime;
      return true;
    }

    function renderCalendar(month, year) {
      const dateContainer = document.getElementById('calendarDates');
      const monthName = new Date(year, month).toLocaleString('default', { month: 'long' });
      document.getElementById('calendarMonth').textContent = `${monthName} ${year}`;
      dateContainer.innerHTML = '';

      const firstDay = new Date(year, month, 1).getDay();
      const daysInMonth = new Date(year, month + 1, 0).getDate();
      const startOffset = firstDay === 0 ? 6 : firstDay - 1;

      for (let i = 0; i < startOffset; i++) {
        dateContainer.innerHTML += `<div></div>`;
      }

      for (let day = 1; day <= daysInMonth; day++) {
        const btn = document.createElement('button');
        btn.type = "button";
        btn.textContent = day;
        btn.onclick = () => {
          document.querySelectorAll('.calendar-dates button').forEach(b => b.classList.remove('selected'));
          btn.classList.add('selected');
          selectedDate = `${year}-${String(month + 1).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
        };
        dateContainer.appendChild(btn);
      }
    }

